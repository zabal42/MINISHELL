#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}

//liberamos con del el contenido del nodo
//liberamos con free toda la memoria del nodo
//solo lo hacemos en ese nodo!