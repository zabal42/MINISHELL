#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
// Recorre todos los nodos de la lista
// Aplica la función f al contenido del nodo (vagón) actual
// Avanza al siguiente nodo