#include "libft.h"
#include <stddef.h>

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
//mientras despues de lst actual exista otro actualiza el nodo a esa posicion
//en el momento que en ligar de next exista NULL, se para y da la posicion