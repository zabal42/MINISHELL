#include "libft.h"
#include <stddef.h>

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
}
//el (*lst)-> es el enganche al siguiente nodo (lo que antes haciamos ++)
